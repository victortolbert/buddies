<template>
  <article class="flex flex-col mb-16 overflow-hidden rounded shadow last:mb-0">
    <nuxt-link :to="`/${article.slug}`" class="flex-shrink-0">
      <img
        class="object-cover w-full"
        :src="`https://nuxtjs.org/${article.imgUrl}`"
        alt
      />
    </nuxt-link>

    <div
      class="flex flex-col justify-between flex-1 p-8 bg-white dark:bg-gray-700"
    >
      <div class="flex-1">
        <p class="leading-none">
          <span
            v-for="tag of article.tags"
            :key="tag"
            class="inline-flex items-center px-2 py-1 mr-2 text-xs font-medium leading-4 bg-green-100 rounded-full  dark:bg-nuxt-lightgreen text-nuxt-lightgreen dark:text-white"
            >{{ tag }}</span
          >
        </p>
        <nuxt-link :to="`/${article.slug}`" class="block">
          <h3
            class="mt-2 text-xl font-semibold leading-7 text-gray-900  dark:text-white"
          >
            {{ article.title }}
          </h3>
          <p class="mt-3 text-base leading-6 text-gray-600 dark:text-gray-400">
            {{ article.description }}
          </p>
        </nuxt-link>
      </div>
      <div class="flex items-center mt-6">
        <div class="flex-shrink-0">
          <a :href="article.authors[0].link" target="_blank">
            <img
              class="w-10 h-10 rounded-full"
              :src="article.authors[0].avatarUrl"
              alt
            />
          </a>
        </div>
        <div class="ml-3">
          <p
            class="text-sm font-medium leading-5 text-gray-900 dark:text-white"
          >
            <a
              :href="article.authors[0].link"
              target="_blank"
              class="hover:underline"
              >{{ article.authors[0].name }}</a
            >
          </p>
          <div class="flex text-sm leading-5 text-gray-500">
            <time datetime="2020-03-16">{{
              $moment(article.date).format('LL')
            }}</time>
            <span class="mx-1">&middot;</span>
            <span>{{ readingTime }} min read</span>
          </div>
        </div>
      </div>
    </div>
  </article>
</template>

<script>
export default {
  props: {
    article: {
      type: Object,
      required: true,
    },
  },
  computed: {
    readingTime() {
      return Math.ceil(
        this.$moment.duration(this.article.readingTime).asMinutes()
      )
    },
  },
}
</script>
